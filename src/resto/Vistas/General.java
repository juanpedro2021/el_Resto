    public void cargarTablaDistriMesas() {
        borrarFilasReserva();
        MesaData mesad = new MesaData();
        String estadoM = "";
        for (Mesa mesa : mesad.listarMesas()) {

            if (mesa.getEstado() == 1) {

                estadoM = "Reservada";
            } else if (mesa.getEstado() == 2) {

                estadoM = "Ocupada";
            } else {

                estadoM = "Libre";
            }

            modeloM.addRow(new Object[]{ mesa.getIdMesa(),mesa.getNumMesa(),mesa.getCapacidad(),estadoM});

        }
    }    

